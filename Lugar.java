public class Lugar {

	private String tipoCancha;
	private Double medidaDeRed;
	private Double anchoDeCancha;
	private Double largoDeCancha;

	public String getTipoCancha() {
		return this.tipoCancha;
	}

	/**
	 * 
	 * @param tipoCancha
	 */
	public void setTipoCancha(String tipoCancha) {
		this.tipoCancha = tipoCancha;
	}

	public Double getMedidaDeRed() {
		return this.medidaDeRed;
	}

	/**
	 * 
	 * @param medidaDeRed
	 */
	public void setMedidaDeRed(Double medidaDeRed) {
		this.medidaDeRed = medidaDeRed;
	}

	public Double getAnchoDeCancha() {
		return this.anchoDeCancha;
	}

	/**
	 * 
	 * @param anchoDeCancha
	 */
	public void setAnchoDeCancha(Double anchoDeCancha) {
		this.anchoDeCancha = anchoDeCancha;
	}

	public Double getLargoDeCancha() {
		return this.largoDeCancha;
	}

	/**
	 * 
	 * @param largoDeCancha
	 */
	public void setLargoDeCancha(Double largoDeCancha) {
		this.largoDeCancha = largoDeCancha;
	}

}