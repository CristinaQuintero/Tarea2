public class Tiempos {

	public int cambioDeCampo;
	public int descanso;
	public int numeroDeSet;

	public int getCambioDeCampo() {
		return this.cambioDeCampo;
	}

	/**
	 * 
	 * @param cambioDeCampo
	 */
	public void setCambioDeCampo(int cambioDeCampo) {
		this.cambioDeCampo = cambioDeCampo;
	}

	public int getDescanso() {
		return this.descanso;
	}

	/**
	 * 
	 * @param descanso
	 */
	public void setDescanso(int descanso) {
		this.descanso = descanso;
	}

	public int getNumeroDeSet() {
		return this.numeroDeSet;
	}

	/**
	 * 
	 * @param numeroDeSet
	 */
	public void setNumeroDeSet(int numeroDeSet) {
		this.numeroDeSet = numeroDeSet;
	}

}